     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // echo "working";
        // dd($request);
        
        $c = new Contact;
        $c->user_id = Auth::id();
        $c->name = $request->name;
        $c->email = $request->email;
        $c->message = $request->message;
        $c->save();
        return back()->with('message',"Thank You. We will get back to you soon.");
    }

